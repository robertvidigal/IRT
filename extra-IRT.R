# Item Characteristic Curve (ICC)
- A curva característica do item (ou IRF) é um dos componentes básicos da teoria de resposta ao item. 
- IRT trata a dificuldade de cada item (os ICCs) como informaçãoa ser incorporada nos itens de escala. 
- Isso distingue a análise de IRT de, por exemplo, a escala Likert, na qual todos os itens são considerados réplicas uns dos outros ou, em outras palavras, os itens são considerados instrumentos paralelos (i.e., feeling thermometers).
  - Rensis Likert presumia que cada item estava monotonicamente relacionado à característica subjacente. 
  - Por isso, as escalas likert tendem a ser específicas da amostra, o que torna complicada a comparação de escalas likert entre amostras.

