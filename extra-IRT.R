# Item Information Curve (IIC)
Characterizing the accuracy of test scores is perhaps the central issue in psychometric theory. 

Classical test theory (CTT) and IRT are largely concerned with the same problems but are different bodies of theory and entail different methods. 

Although the two paradigms are generally consistent and complementary, there are a number of points of difference:

An important difference between CTT and IRT is the treatment of measurement error, indexed by the standard error of measurement. 

All tests, questionnaires, and inventories are imprecise tools; we can never know a persons true score, but rather only have an estimate, the observed score. 

There is some amount of random error which may push the observed score higher or lower than the true score. 
**CTT assumes that the amount of error is the same for each examinee, but IRT allows it to vary.**

One of the major contributions of item response theory is the extension of the concept of reliability. 
Traditionally, reliability refers to the precision of measurement (i.e., the degree to which measurement is free of error).

---
In the place of reliability, IRT offers the test information function which shows the degree of precision at different values of theta.

- Item information curve (IIC): the amount of information the response to an item provides about a person parameter. 
- For test information, this is the amount of information all of the items encountered by a person provide about a person parameter.

In general, item information functions tend to look bell-shaped. 
The flatter the curve, the less the item is able to discriminate since the probability of correct response at low ability levels is
nearly the same as it is at high ability levels. Using these two descriptors, one can describe the general form of the item characteristic curve. 

Information is also a function of the model parameters:

Highly discriminating items have tall, narrow information functions; they contribute greatly but over a narrow range. 
Less discriminating items provide less information but over a wider range.
